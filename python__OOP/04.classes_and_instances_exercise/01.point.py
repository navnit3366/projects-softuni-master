import math


class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def set_x(self, new_x):
        self.x = new_x

    def set_y(self, new_y):
        self.y = new_y

    def distance(self, x, y):
        xx = abs(self.x - x)
        yy = abs(self.y - y)
        c = (xx ** 2) + (yy ** 2)

        return math.sqrt(c)
